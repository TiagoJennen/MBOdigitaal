<h2 class="text-lg font-bold text-gray-800">Orientatie op ondernemerschap</h2><br>
<p class="mt-2 text-gray-700"><strong>1. Algemene informatie</strong><br>
<br>
Studielast: 240 uur<br>
Beroepsvereisten: Nee<br>
Certificaten: Nee<br>
Relevantie: Het keuzedeel is relevant voor mbo-niveau 4 en biedt studenten op niveau 2 en 3 een lichtere versie van het CE Ondernemerschap om hen bekend te maken met de beginselen van ondernemerschap.<br>
</p><br>
<p class="mt-2 text-gray-700"><strong>2. Beschrijving</strong><br>
<br>
De beroepsbeoefenaar bedenkt welke activiteiten nodig zijn om klanten te trekken, te houden en te bedienen. Hij probeert waar mogelijk verschillende activiteiten uit en bepaalt het effect ervan. Hij onderzoekt welke praktische zaken geregeld moeten worden voor het runnen van de onderneming, zoals partners zoeken en financiële administratie. Hij bespreekt zijn ideeën met betrokkenen of probeert zaken uit door bijvoorbeeld mee te kijken met een ondernemer of zelf te oefenen.
<br>
Resultaat:<br>
<br>
Inzicht in de basisbeginselen van bedrijfsvoering, zoals financiële gegevens beheren, kansen en bedreigingen inschatten, en prioriteiten stellen.<br>
<br>
Gedrag:

Stelt zich onderzoekend en lerend op om inzicht of ervaring te krijgen met de taken van een ondernemer.
Combineert creativiteit met realiteitszin bij het bedenken van mogelijkheden om klanten te trekken, boeien en binden.
Laat zien over een ondernemende houding en commercieel en bedrijfsmatig inzicht te beschikken, of toont zelfinzicht door aan te geven wat hij nog verder moet ontwikkelen voor ondernemerschap.
