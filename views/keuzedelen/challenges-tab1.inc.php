<h2 class="text-lg font-bold text-gray-800">Basis programmeren van games</h2><br>
<p class="mt-2 text-gray-700"><strong>1. Algemene informatie</strong><br>
<br>
Studielast: 240 uur.<br>
Beroepsvereisten en certificaten: Nee<br>
Toelichting: Je hebt wel basiskennis van programmeren nodig om dit keuzedeel goed te kunnen volgen.<br>
Relevantie: Bedrijven, vooral kleine en middelgrote ondernemingen, zoeken steeds vaker naar programmeurs die ook eenvoudige games kunnen maken, bijvoorbeeld voor mobiele apparaten of educatieve toepassingen. Er is vraag naar allround programmeurs met kennis van gameprogrammeren.<br>
</p><br>
<p class="mt-2 text-gray-700"><strong>2. Wat leer je in dit keuzedeel?</strong><br>
<br>
Het gaat over het programmeren van eenvoudige games met behulp van een game engine. Het doel is om je programmeervaardigheden en creativiteit te ontwikkelen.</p><br>
<p class="mt-2 text-gray-700"><strong>3. De opdrachten die je uitvoert:</strong><br>
<br>
D1-K1: Maakt een eenvoudige game
Complexiteit: Je gebruikt specialistische kennis van game-ontwikkelomgevingen en programmeertalen om een eenvoudige game te maken. Je werkt in een creatief team, waar je verantwoordelijk bent voor de technische uitvoering van de game.
Vakkennis en vaardigheden: Je leert werken met game engines en de syntax van programmeertalen. Daarnaast leer je commercieel te denken, bijvoorbeeld door rekening te houden met verkoopdoelen.<br>
<br>
D1-K1-W1: Voorbereiding van de technische realisatie
Je helpt bij het opstellen van het Game Design Document (GDD), waarin je advies geeft over de technische kant van de game.
Je richt de ontwikkelomgeving in en plant de werkzaamheden.
Het resultaat is een goed GDD en een werkende ontwikkelomgeving voor de game.<br>
<br>
D1-K1-W2: Realiseren en testen van de game
Je schrijft de code voor de game in een editor en voegt onderdelen zoals licht, geluid en beweging toe in de game engine.
Na het schrijven van de code, test je de game en verbeter je deze waar nodig.
Het resultaat is een goed werkende eenvoudige game.<br>
<br>
D1-K1-W3: Oplevering van de game
Je levert de gemaakte game op, demonstreert deze aan de opdrachtgever, en past eventueel verbeteringen toe.
Je documenteert je werk zorgvuldig.
Het resultaat is een opgeleverde game en bijbehorende documentatie.
Dit keuzedeel helpt je om vaardigheden te ontwikkelen die nuttig zijn voor gameontwikkeling, maar ook breder toepasbaar zijn in de IT-wereld.
