<h2 class="text-lg font-bold text-gray-800">Verdieping software</h2><br>
<p class="mt-2 text-gray-700"><strong>1. Algemene informatie</strong><br>
<br>
Studielast: 240 uur<br>
Beroepsvereisten: Nee<br>
Certificaten: Nee<br>
Relevantie: Dit keuzedeel richt zich op de verdieping in software die relevant is voor verschillende beroepen, niet alleen ICT, maar ook administratieve beroepen. Het is bedoeld om de beginnende beroepsbeoefenaar specialistische kennis van software te geven, wat hen aantrekkelijker maakt voor de arbeidsmarkt. De beroepsbeoefenaar kan zelf kiezen of hij buiten dit keuzedeel om een extern examen wil behalen.
Dit keuzedeel kan breed worden toegepast, afhankelijk van de sector waarin de beroepsbeoefenaar zich wil specialiseren.
<br>
</p><br>
<p class="mt-2 text-gray-700"><strong>2. Beschrijving</strong><br>
<br>
Het gaat over het programmeren van eenvoudige games met behulp van een game engine. Het doel is om je programmeervaardigheden en creativiteit te ontwikkelen.</p><br>
Complexiteit:
De beroepsbeoefenaar verdiept zich in software die specialistisch of breed kan zijn, gebaseerd op eerder aangeleerde kennis of nieuwe software die gerelateerd is aan de aangeboden stof. Het zelfstandig bepalen van het leertraject maakt dit werk complex.<br>
<br>
Verantwoordelijkheid en zelfstandigheid:
De beroepsbeoefenaar kiest zelf software(pakketten) en bespreekt deze met zijn leidinggevende. Hij is verantwoordelijk voor zijn leerproces en de resultaten van zijn keuzes.<br>
<br>
Vakkennis en vaardigheden:
<br>
Kennis van de arbeidsmarktrelevantie van software in zijn vakgebied
Actuele en specialistische kennis van gekozen software
Brede kennis van het leerproces om software eigen te maken
Vaardigheden in onderhandeling en overtuiging tijdens overleg met de leidinggevende<br>
