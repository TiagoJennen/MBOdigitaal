<h1 class="m-2 text-2xl leading-none text-gray-900">
    Bijlagen
=======
<h2 class="text-lg font-bold text-gray-800">Fotografie basis</h2><br>
<p class="mt-2 text-gray-700"><strong>1. Algemene informatie</strong><br>
<br>
Studielast: 240 uur<br>
Beroepsvereisten: Geen<br>
Certificaten: Geen<br>
Gekoppeld aan kwalificatie(s): Zie bijlage op www.s-bb.nl/kwalificatiedossiers<br>
Toelichting:  Dit keuzedeel is van belang voor de arbeidsmarkt omdat het beroepsbeoefenaren in staat stelt om in te spelen op trends en ontwikkelingen binnen hun branche. Het helpt hen bij het bijhouden van innovaties en veranderingen, wat cruciaal is om relevant te blijven in een snel veranderende omgeving.<br>
Beschrijving: Het keuzedeel richt zich op het verbeteren van de vaardigheid om te reageren op innovaties en veranderingen in de branche door reflectie op het eigen beroepsmatig handelen.<br>
Branchevereisten: Geen<br>
Aard van keuzedeel: Generiek<br>
</p><br>
<p class="mt-2 text-gray-700"><strong>2. Uitwerking</strong><br>
<br>
Complexiteit: De werkzaamheden zijn grotendeels routinematig en volgen standaardprocedures. Technische handelingen zoals het installeren van fotoapparatuur en het oplossen van storingen zijn complex en vereisen technische kennis. Verschillende producten en locaties vereisen verschillende camera-instellingen.
Verantwoordelijkheid en Zelfstandigheid: De beginnend beroepsbeoefenaar voert voornamelijk uitvoerende taken uit, zoals het maken van functionele foto's. Taken worden op basis van instructies van de leidinggevende uitgevoerd, soms zelfstandig bij eenvoudige opdrachten. Bij technische problemen is de leidinggevende het aanspreekpunt, en bij complexe opnames assisteert de beginnend beroepsbeoefenaar de leidinggevende. Er is verantwoordelijkheid voor het zorgvuldig omgaan met dure apparatuur.<br>
<br>
Vakkennis en Vaardigheden:<br>
<br>
Basiskennis van daglicht en kunstlicht voor het maken van functionele foto's.
<br>
Basiskennis van product- en locatiefotografie.
<br>
Basiskennis van diafragma, sluitertijd en lichtgevoeligheid.
<br>
Basiskennis van verschillende foto-bestandsformaten.
<br>
Het kunnen instellen en bedienen van een fotocamera.
<br>
Het kunnen uitvoeren van bewerkingen met fotobewerkingssoftware.
<br>
Het kunnen afdrukken, printen, inlijsten of digitaal aanleveren van foto's.
<br>
Het kunnen digitaal archiveren van fotobestanden.<br>
<br>
<p class="mt-2 text-gray-700"><strong>3. De opdrachten die je uitvoert:</strong><br>
<br>
Omschrijving:<br>
De beginnend beroepsbeoefenaar verzamelt en controleert de benodigde materialen voor de opname, zorgt ervoor dat alles tijdig op de locatie is, bouwt de fotoapparatuur op, en neemt maatregelen om schade of diefstal te voorkomen. Bij technische problemen meldt hij deze bij de leidinggevende en zoekt een verantwoorde oplossing. Na de opnames demonteert hij de apparatuur en bergt deze op.<br>
<br>
Resultaat:<br>
De fotoapparatuur is compleet, functioneert correct en veilig volgens plan, en is na afloop opgeruimd.<br>
<br>
Gedrag:<br>
Verzamelt materialen zorgvuldig op basis van de paklijst en aanwijzingen van de opdrachtgever.<br>
Houdt zich aan afspraken met betrokkenen.<br>
Hanteert de fotoapparatuur met zorg om beschadiging of diefstal te voorkomen.<br>
Lost problemen of storingen vindingrijk op.<br>
Presteert kalm, effectief en veilig onder tijdsdruk, zodat de voortgang van de opname niet in gevaar komt.<br>
<br>
Onderliggende Competenties: <br>
Samenwerken en overleggen
<br>
Materialen en middelen inzetten
<br>
Met druk en tegenslag omgaan
<br>
Instructies en procedures opvolgen
