<br>
Studielast: 240 uur<br>
Beroepsvereisten: Nee<br>
Certificaten: Ja<br>
Ingangsdatum certificaat: 01-10-2016<br>
Gekoppeld aan kwalificatie(s): Zie bijlage op www.s-bb.nl/kwalificatiedossiers<br>
</p><br>
<p class="mt-2 text-gray-700"><strong>2. Relevantie</strong><br>
<br>
Doelgroep: Alle mbo kwalificatiedossiers waar digitale informatie, gegevens en content worden verzameld, geproduceerd en beheerd.<br>
Niveau: Geschikt voor niveau 3 en 4 kwalificaties.<br>
<br>
<p class="mt-2 text-gray-700"><strong>3. Beschrijving</strong><br>
<br>
Beschrijving
Focus: Kennis, vaardigheden en competenties voor het effectief omgaan met digitale informatie en systemen, ongeacht softwarepakket of werkwijze.
Verschil met basiskeuzedeel: Meer complexe werkprocessen en strategische toepassingen.<br>
<br>
<p class="mt-2 text-gray-700"><strong>4. Kernonderdelen</strong><br>
<br>
Verzamelen van informatie, gegevens en content<br>
<br>
Complexiteit: Standaard en routinematige stappen, evalueren van gegevens en strategieën ontwikkelen.
Verantwoordelijkheid: Zelfstandig, verantwoordelijkheid voor verzamelde informatie en overdracht naar anderen.
Vakkennis: Kennis van ICT, ergonomie, veilig digitaal werken, auteursrechten, en internet/media.
Produceren van informatie/content<br>
<br>
Complexiteit: Diversiteit in taken, context en systemen. Hoge zelfstandigheid en verantwoordelijkheid voor eigen werkresultaten.
Vakkennis: Kennis van ICT, ergonomie, veilig digitaal werken, auteursrechten, digitale omgangsvormen, en sociale media.
Beheren van informatie/content<br>
<br>
Complexiteit: Zelfstandigheid in organiseren en beheren, met aandacht voor procedures en tijdbeheer.
Verantwoordelijkheid: Hoog, inclusief de terugvindbaarheid en bewaring van informatie.
Vakkennis: Kennis van ICT, ergonomie, informatiebeheer, en bewaring van gegevens.<br>
<br>
<p class="mt-2 text-gray-700"><strong>5. Gedragscriteria</strong><br>
<br>
Nauwkeurigheid: Bij het winnen, produceren en beheren van informatie moet nauwkeurig worden gewerkt.<br>
<br>
Zelfstandigheid: Hoge mate van zelfstandigheid vereist bij het uitvoeren van taken.<br>
<br>
Kwaliteit en Effectiviteit: Continu controleren van kwaliteit en efficiëntie van de uitgevoerde werkzaamheden.<br>
